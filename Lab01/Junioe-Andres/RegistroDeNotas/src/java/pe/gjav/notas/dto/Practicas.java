package pe.gjav.notas.dto;

public class Practicas {

    protected int N1;
    protected int N2;
    protected int N3;
    protected int N4;

    public Practicas(int N1, int N2, int N3, int N4) {
        this.N1 = N1;
        this.N2 = N2;
        this.N3 = N3;
        this.N4 = N4;
    }

    public Practicas() {
    }

    public int getN1() {
        return N1;
    }

    public void setN1(int N1) {
        this.N1 = N1;
    }

    public int getN2() {
        return N2;
    }

    public void setN2(int N2) {
        this.N2 = N2;
    }

    public int getN3() {
        return N3;
    }

    public void setN3(int N3) {
        this.N3 = N3;
    }

    public int getN4() {
        return N4;
    }

    public void setN4(int N4) {
        this.N4 = N4;
    }
    
}
